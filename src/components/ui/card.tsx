import * as React from "react"

import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { 
    variant?: "default" | "elevated" | "accent" | "bordered" | "gradient" 
  }
>(({ className, variant = "default", ...props }, ref) => {
  const variants = {
    default: "rounded-lg border bg-card text-card-foreground shadow-sm",
    elevated: "rounded-lg border-0 bg-card text-card-foreground shadow-md hover:shadow-lg transition-shadow",
    accent: "rounded-lg border-accent border-2 bg-card text-card-foreground shadow-sm",
    bordered: "rounded-lg border-2 bg-card text-card-foreground border-primary/20",
    gradient: "rounded-lg border-0 bg-gradient-secondary text-card-foreground shadow-md"
  }
  
  return (
    <div
      ref={ref}
      className={cn(
        variants[variant as keyof typeof variants],
        className
      )}
      {...props}
    />
  )
})
Card.displayName = "Card"

